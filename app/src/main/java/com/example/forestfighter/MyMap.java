    public static int[][] getMap(int level)
    {
        if(level > Constant.GAME_LEVEL || level <0)	//最多十级
        {
            return null;
        }

        int[][] brick = new int[Constant.BRICK_X][Constant.BRICK_Y];

        for (int x = 0; x < Constant.BRICK_X; x++)
        {
            for (int y = 0; y < Constant.BRICK_Y; y++)
            {
                brick[y][x] = bricks[level-1][x][y];
            }
        }
        return brick;
    }
